
public class TareaNumerica extends Tarea{
	//---Atributos------------------------------------------------------------------------------------------------------
	private int numero;
	//------------------------------------------------------------------------------------------------------------------


	//---Constructores--------------------------------------------------------------------------------------------------
	public TareaNumerica() {
	}

	public TareaNumerica(String taskName) {
		super(taskName);
	}
	//------------------------------------------------------------------------------------------------------------------


	//---Getters y Setters----------------------------------------------------------------------------------------------
	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	//------------------------------------------------------------------------------------------------------------------


	//---toString-------------------------------------------------------------------------------------------------------
	@Override
	public String toString() {
		return super.toString() +
				"Numero: " + numero;
	}
	//------------------------------------------------------------------------------------------------------------------
}
