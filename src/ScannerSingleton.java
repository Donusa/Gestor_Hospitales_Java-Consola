import java.util.Scanner;

public class ScannerSingleton{
    //---Atributos------------------------------------------------------------------------------------------------------
    private static Scanner sc = null;
    //------------------------------------------------------------------------------------------------------------------


    //---Constructores--------------------------------------------------------------------------------------------------
    private ScannerSingleton() {
    	
    }
    //------------------------------------------------------------------------------------------------------------------


    //---Metodos--------------------------------------------------------------------------------------------------------
    public static synchronized Scanner getInstance() {
        if (sc == null) {
        	sc = new Scanner(System.in);
        }
        return sc;
    }
    //------------------------------------------------------------------------------------------------------------------
}