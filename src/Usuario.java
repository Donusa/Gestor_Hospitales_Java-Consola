
public class Usuario {
	
	
	String userName;
	String email;
	String password;
	String userDni;
	String userCel;
	
	public Usuario(String userName, String email, String password, String userDni, String userCel) {	
		this.userName = userName;
		this.email = email;
		this.password = password;
		this.userDni = userDni;
		this.userCel = userCel;
	}
	
	
}
